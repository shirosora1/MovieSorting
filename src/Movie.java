public class Movie {
    String title;
    int releaseYear;
    double ratingImdb;
    int ageRating;

    public Movie(String title, int releaseYear, double ratingImdb, int ageRating) {
        this.title = title;
        this.releaseYear = releaseYear;
        this.ageRating = ageRating;
        this.ratingImdb = ratingImdb;
    }
}
